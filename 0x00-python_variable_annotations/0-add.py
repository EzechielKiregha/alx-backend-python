#!/usr/bin/env python3
"""
This module will implicitly add 2 floating point numbers
and float sum
"""


def add(a: float, b: float) -> float:
    """Add to floating point numbers

    Args:
        a (float): can only be a float
        b (float): can not anything but float

    Returns:
        float: the sum of the a and b
    """
    return a + b
