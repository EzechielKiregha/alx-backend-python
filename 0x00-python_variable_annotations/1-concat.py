#!/usr/bin/env python3
"""
    This module returns concatinated String from 2 strings
    args
"""


def concat(str1: str, str2: str) -> str:
    """Concatinate 2 strings

    Args:
        str1 (str): can only be a string
        str2 (str): cannot be anything but a string

    Returns:
        str: the concatinated string
    """
    return str1 + str2
