#!/usr/bin/env python3
"""
this is a module that takes in a float
and return its floor (rounded value)
"""


def floor(n: float) -> int:
    """it takes in one parameter a float
    and returns its floor value

    Args:
        n (float): n is expected to be a float

    Returns:
        int: the integer casted value of n
    """
    return int(n)
